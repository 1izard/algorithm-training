#include <bits/stdc++.h>
using namespace std;

int main() {
  printf("%d\n", __gcd(12, 8));
  printf("%d\n", __gcd(-12, 8));
  printf("%d\n", __gcd(12, -8));
  printf("%d\n", __gcd(-12, -8));
  printf("\n");
  printf("%d\n", __gcd(2, 1));
  printf("%d\n", __gcd(-2, 1));
  printf("%d\n", __gcd(2, -1));
  printf("%d\n", __gcd(-2, -1));
}